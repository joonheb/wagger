package com.example.afinal.gameModel;

public class LadderPathColorModel {
    float x, y;

    LadderPathColorModel() {}
    public LadderPathColorModel(float x, float y) {
        this.x  = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
